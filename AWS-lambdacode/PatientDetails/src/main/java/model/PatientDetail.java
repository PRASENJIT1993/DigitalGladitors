package model;

public class PatientDetail {	
	String accident;
	String address;
	String age;
	String bloodGroup;
	String dateOfBirth;
	String emergencyContact;
	String emergencyEmailId;
	String mainAreasOfSymptoms;
	String patientId;
	String patientName;
	String phoneNumber;
	String reasonForVisit;
	
	public String getAccident() {
		return accident;
	}
	public void setAccident(String accident) {
		this.accident = accident;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getBloodGroup() {
		return bloodGroup;
	}
	public void setBloodGroup(String bloodGroup) {
		this.bloodGroup = bloodGroup;
	}
	public String getDateOfBirth() {
		return dateOfBirth;
	}
	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}
	
	public String getEmergencyContact() {
		return emergencyContact;
	}
	public void setEmergencyContact(String emergencyContact) {
		this.emergencyContact = emergencyContact;
	}
	public String getEmergencyEmailId() {
		return emergencyEmailId;
	}
	public void setEmergencyEmailId(String emergencyEmailId) {
		this.emergencyEmailId = emergencyEmailId;
	}
	public String getMainAreasOfSymptoms() {
		return mainAreasOfSymptoms;
	}
	public void setMainAreasOfSymptoms(String mainAreasOfSymptoms) {
		this.mainAreasOfSymptoms = mainAreasOfSymptoms;
	}
	public String getPatientId() {
		return patientId;
	}
	public void setPatientId(String patientId) {
		this.patientId = patientId;
	}
	public String getPatientName() {
		return patientName;
	}
	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getReasonForVisit() {
		return reasonForVisit;
	}
	public void setReasonForVisit(String reasonForVisit) {
		this.reasonForVisit = reasonForVisit;
	}
	
	
}
